//----------------------------------------------------------------------------//
// Constants                                                                  //
//----------------------------------------------------------------------------//
const METABALLS_VERSION_MAJOR = 1;
const METABALLS_VERSION_MINOR = 0;
const METABALLS_VERSION_BABY  = 0;

//----------------------------------------------------------------------------//
// Public Functions                                                           //
//----------------------------------------------------------------------------//
//------------------------------------------------------------------------------
function GetVersion()
{
    return String_Cat(
        METABALLS_VERSION_MAJOR, ".",
        METABALLS_VERSION_MINOR, ".",
        METABALLS_VERSION_BABY
    );
}
